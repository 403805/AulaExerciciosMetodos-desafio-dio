package dio.innovation.basecamp02;

public class Calculadora {

    public static void soma() {

        System.out.println(" Adição ");
        double n1 = 5.5, n2 = 9;
        double resultado = n1 + n2;
        System.out.println("Resultado da soma de " + n1 + " + " +n2 + " é = " + resultado);

    }

    public static void subtracao() {

        System.out.println(" Subtração ");
        int n1 = 4, n2 = 7;
        double resultado = n1 - n2;
        System.out.println("Resultado da subtração de " + n1 +" - " + n2 + " é = " + resultado);

    }

    public static void multiplicacao() {

        System.out.println(" Multiplicação ");

        double n1 = 3.5, n2 = 7.1;
        double resultado = n1 * n2;
        System.out.println("Resultado da multiplicação de " + n1 +" x " +n2 + " é = " + resultado);

    }

    public static void divisao() {
        System.out.println(" Divisão ");
        double n1 = 8.5, n2 = 78.2;
        double resultado = n1 / n2;
        System.out.println("Resultado da divisão de " + n1 +" / " + n2 + " é = " + resultado);

    }

}









